package java04;

public class jv04_03_삼각형출력 {
    public static void main(String[] args){
        
        
        for(int i=1; i<=5; i=i+1){//for문 작성 시작값과 종료 값을 넣는다.
            for(int j=1; j<=i; j=j+1){//j<=i값을 넣어 종료될때까지 반복하게 한다.
               
                    System.out.print("*");//별이 반복되며 옆으로 출력하게 한다.
                }
            System.out.println("");//줄바꿈을 해줘서 다시 for문으로 돌아가게 한다.
                }
             
            }
        
        }
   