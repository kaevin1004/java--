package java02;

public class jv02_12_ASC코드 {
    public static void main(String[] args) {
        int x = '4';
        int y = '5';
        int result = 0;
        
        System.out.println(x);
        System.out.println(y);
        
        result = x + y;
        System.out.println(result);
        
        result = x - y;
        System.out.println(result);
        
        result = x * y;
        System.out.println(result);
        
        result = x / y;
        System.out.println(result);
        
        result = x % y;
        System.out.println(result);
    }
    
}
