package java02;

public class jv02_01_for {
    public static void main(String[] args) {
        
        for(int i=1;i<=10;i++){          //항상 뒤에 "{}"을 적는다.
            System.out.println(i);
        }
            
        
        for(int n=1;n<=100;n*=2){        //항상 뒤에 "{}"을 적는다.
            System.out.println(n);
        }
    }
    
}
